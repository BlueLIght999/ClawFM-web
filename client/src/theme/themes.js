export const THEME_NAMES = ['cream', 'morning', 'afternoon', 'night'];

export function getTimeTheme(hours) {
  if (hours >= 6 && hours < 14) return 'morning';
  if (hours >= 14 && hours < 18.5) return 'afternoon';
  return 'night';
}

